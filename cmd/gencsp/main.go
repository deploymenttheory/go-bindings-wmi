// Command gencsp turns committed CSP snapshots (metadata/csp/*.json, from
// cmd/fetchddf) into typed Go policy bindings under bindings/csp/<area>:
// one csp.Policy descriptor per leaf node plus enum constants for its
// allowed values. Self-cleaning and byte-deterministic — the CSP analogue
// of cmd/generate.
//
//	go run ./cmd/gencsp
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"slices"
	"sort"
	"strings"

	"github.com/deploymenttheory/go-bindings-wmi/internal/cspschema"
)

const header = "// Code generated by go-bindings-wmi-csp-codegen. DO NOT EDIT.\n"

func main() {
	metadataDir := "metadata/csp"
	outDir := filepath.Join("bindings", "csp")
	if len(os.Args) > 1 {
		metadataDir = os.Args[1]
	}
	if err := run(metadataDir, outDir); err != nil {
		fmt.Fprintln(os.Stderr, "gencsp:", err)
		os.Exit(1)
	}
}

func run(metadataDir, outDir string) error {
	entries, err := filepath.Glob(filepath.Join(metadataDir, "*.json"))
	if err != nil {
		return err
	}
	sort.Strings(entries)
	if len(entries) == 0 {
		return fmt.Errorf("no CSP snapshots in %s (run cmd/fetchddf)", metadataDir)
	}

	bridgeAreas, err := loadBridgeAreas(metadataDir)
	if err != nil {
		return err
	}
	cspClasses, err := loadBridgeCSPClasses(metadataDir)
	if err != nil {
		return err
	}

	// Pass 1: load CSPs and claim package names (fail fast on collision).
	type area struct {
		pkg string
		csp *cspschema.CSP
	}
	var areas []area
	packages := map[string]string{}
	for _, entry := range entries {
		base := strings.TrimSuffix(filepath.Base(entry), ".json")
		if base == "PROVENANCE" || base == bridgeFile || base == bridgeCSPFile {
			continue
		}
		data, err := os.ReadFile(entry)
		if err != nil {
			return err
		}
		var csp cspschema.CSP
		if err := json.Unmarshal(data, &csp); err != nil {
			return fmt.Errorf("%s: %w", entry, err)
		}
		pkg := packageName(base, csp.PolicyArea())
		if prev, taken := packages[pkg]; taken {
			return fmt.Errorf("package collision: %s and %s both map to %q", prev, base, pkg)
		}
		packages[pkg] = base
		areas = append(areas, area{pkg, &csp})
	}

	written := map[string]bool{}
	for _, a := range areas {
		files, err := renderPackage(a.pkg, a.csp, bridgeAreas, cspClasses)
		if err != nil {
			return fmt.Errorf("%s: %w", a.csp.Name, err)
		}
		if len(files) == 0 {
			continue // no leaf policies (pure grouping CSP)
		}
		pkgDir := filepath.Join(outDir, a.pkg)
		if err := os.MkdirAll(pkgDir, 0o755); err != nil {
			return err
		}
		for name, body := range files {
			path := filepath.Join(pkgDir, name)
			if err := os.WriteFile(path, body, 0o644); err != nil {
				return err
			}
			written[path] = true
		}
	}
	fmt.Printf("generated %d CSP packages → %s\n", len(packages), outDir)
	return pruneStale(outDir, written)
}

// policy is a flattened leaf node with its generated Go name and the path
// segments below the CSP root (segs; a direct area setting has one segment).
type policy struct {
	goName string
	segs   []string
	node   cspschema.Node
}

func renderPackage(pkg string, csp *cspschema.CSP, bridgeAreas map[string]string, cspClasses map[string][]cspBridge) (map[string][]byte, error) {
	policies := uniquify(flatten(csp.Nodes, nil))
	if len(policies) == 0 {
		return nil, nil
	}

	// Resolve the bridge area for this CSP: a native Policy area whose name
	// the captured bridge exposes. Its direct settings become executable.
	bridgeArea := ""
	if csp.PolicyArea() {
		bridgeArea = bridgeAreas[strings.ToLower(csp.Name)]
	}

	var policiesBody, constsBody strings.Builder
	all := make([]string, 0, len(policies))
	for _, p := range policies {
		renderPolicy(&policiesBody, p, resolveBridge(p, bridgeArea, csp.Path, cspClasses))
		renderConstants(&constsBody, p)
		all = append(all, p.goName)
	}

	// All: every policy in the area, for enumeration.
	policiesBody.WriteString("// All lists every policy in this CSP area.\n")
	policiesBody.WriteString("var All = []csp.Policy{\n")
	for _, name := range all {
		fmt.Fprintf(&policiesBody, "\t%s,\n", name)
	}
	policiesBody.WriteString("}\n")

	files := map[string][]byte{}
	doc, err := assemble(pkg, fmt.Sprintf("// Package %s binds the %s CSP (%s).\n",
		pkg, csp.Name, csp.Path), "")
	if err != nil {
		return nil, err
	}
	files["doc.go"] = doc
	for name, body := range map[string]string{
		pkg + "_policies.go":  policiesBody.String(),
		pkg + "_constants.go": constsBody.String(),
	} {
		if strings.TrimSpace(body) == "" {
			continue
		}
		src, err := assemble(pkg, "", body)
		if err != nil {
			return nil, fmt.Errorf("%s: %w", name, err)
		}
		files[name] = src
	}
	return files, nil
}

// uniquify drops exact-duplicate URIs (the DDF occasionally repeats a node)
// and gives every remaining policy a unique Go name, suffixing collisions
// deterministically (two distinct URIs whose names sanitize to the same
// identifier).
func uniquify(policies []policy) []policy {
	seenURI := map[string]bool{}
	claimed := map[string]bool{}
	out := policies[:0]
	for _, p := range policies {
		if seenURI[p.node.Path] {
			continue
		}
		seenURI[p.node.Path] = true
		name := p.goName
		for n := 2; claimed[name]; n++ {
			name = fmt.Sprintf("%s%d", p.goName, n)
		}
		claimed[name] = true
		p.goName = name
		out = append(out, p)
	}
	return out
}

// flatten walks the node tree, returning leaf (value-bearing) nodes named by
// their path segments below the CSP root — unique within the area.
func flatten(nodes []cspschema.Node, prefix []string) []policy {
	var out []policy
	for _, n := range nodes {
		segs := append(slices.Clone(prefix), n.Name)
		if n.Leaf() {
			out = append(out, policy{goName: joinExport(segs), segs: segs, node: n})
		}
		out = append(out, flatten(n.Children, segs)...)
	}
	return out
}

func renderPolicy(b *strings.Builder, p policy, spec *bridgeSpec) {
	n := p.node
	if n.Description != "" {
		fmt.Fprintf(b, "// %s: %s\n", p.goName, firstLine(n.Description))
	}
	if n.DeprecatedOSBuild != "" {
		fmt.Fprintf(b, "// Deprecated: as of OS build %s.\n", n.DeprecatedOSBuild)
	}
	fmt.Fprintf(b, "var %s = csp.Policy{\n", p.goName)
	fmt.Fprintf(b, "\tName: %q,\n", n.Name)
	fmt.Fprintf(b, "\tURI: %q,\n", n.Path)
	fmt.Fprintf(b, "\tFormat: %q,\n", n.Format)
	if len(n.Access) > 0 {
		fmt.Fprintf(b, "\tAccess: []string{%s},\n", quoteJoin(n.Access))
	}
	if n.Default != "" {
		fmt.Fprintf(b, "\tDefault: %q,\n", n.Default)
	}
	if n.DeprecatedOSBuild != "" {
		fmt.Fprintf(b, "\tDeprecatedOSBuild: %q,\n", n.DeprecatedOSBuild)
	}
	if n.Applicability.MinOSBuild != "" {
		fmt.Fprintf(b, "\tMinOSBuild: %q,\n", n.Applicability.MinOSBuild)
	}
	if n.Applicability.CSPVersion != "" {
		fmt.Fprintf(b, "\tCSPVersion: %q,\n", n.Applicability.CSPVersion)
	}
	if n.Applicability.Editions != "" {
		fmt.Fprintf(b, "\tEditions: %q,\n", n.Applicability.Editions)
	}
	if n.Applicability.RequiresAAD {
		b.WriteString("\tRequiresAAD: true,\n")
	}
	if av := n.AllowedValues; av != nil {
		fmt.Fprintf(b, "\tAllowed: &csp.Allowed{Type: %q", av.Type)
		if av.ADMX != nil {
			fmt.Fprintf(b, ", ADMX: &csp.ADMXBacking{Area: %q, Name: %q, File: %q}",
				av.ADMX.Area, av.ADMX.Name, av.ADMX.File)
		}
		if len(av.Enum) > 0 {
			b.WriteString(", Enum: []csp.EnumValue{")
			for i, e := range av.Enum {
				if i > 0 {
					b.WriteString(", ")
				}
				fmt.Fprintf(b, "{Value: %q, Description: %q}", e.Value, e.Description)
			}
			b.WriteString("}")
		}
		b.WriteString("},\n")
	}
	if spec != nil {
		fmt.Fprintf(b, "\tBridge: &csp.Bridge{ConfigClass: %q, ResultClass: %q, InstanceID: %q, ParentID: %q, Property: %q},\n",
			spec.configClass, spec.resultClass, spec.instanceID, spec.parentID, spec.property)
	}
	b.WriteString("}\n\n")
}

// bridgeSpec is a resolved DDF↔bridge mapping for one policy.
type bridgeSpec struct {
	configClass, resultClass, instanceID, parentID, property string
}

// dmRoot are the OMA-DM tree scaffolding segments that are not part of a
// bridge class's name.
var dmRoot = map[string]bool{".": true, "Device": true, "User": true, "Vendor": true, "MSFT": true, "": true}

// resolveBridge maps a policy onto the MDM bridge, or returns nil when it is
// not statically executable. Native Policy areas use the regular
// MDM_Policy_Config01_<Area>02 convention (direct settings only); other CSPs
// match their instance-node against the captured bridge classes by
// normalized name, validated by the property. Dynamic-instance nodes ({...})
// and ambiguous matches are skipped.
func resolveBridge(p policy, bridgeArea, cspPath string, cspClasses map[string][]cspBridge) *bridgeSpec {
	if bridgeArea != "" {
		if len(p.segs) != 1 {
			return nil // only an area's direct settings are area-instance properties
		}
		return &bridgeSpec{
			configClass: "MDM_Policy_Config01_" + bridgeArea + "02",
			resultClass: "MDM_Policy_Result01_" + bridgeArea + "02",
			instanceID:  bridgeArea,
			parentID:    bridgeParentID(cspPath),
			property:    p.node.Name,
		}
	}

	// Non-Policy: the instance-node is the leaf's parent; ParentID is that
	// node's parent path, InstanceID its name (verified against device
	// truth). Both keys are scope-relative.
	segs := strings.Split(p.node.Path, "/")
	if len(segs) < 3 {
		return nil
	}
	property := segs[len(segs)-1]
	instanceNode := segs[len(segs)-2]
	if strings.ContainsAny(instanceNode, "{}") {
		return nil // dynamic instance — needs a runtime id
	}
	var meaningful []string
	for _, s := range segs[:len(segs)-1] {
		if dmRoot[s] {
			continue
		}
		if strings.ContainsAny(s, "{}") {
			return nil // dynamic ancestor
		}
		meaningful = append(meaningful, s)
	}
	if len(meaningful) == 0 {
		return nil
	}
	cands := cspClasses[normalizeBridge(strings.Join(meaningful, "_"))]
	if len(cands) != 1 || !cands[0].props[property] {
		return nil // no match, ambiguous, or property not on the class
	}
	return &bridgeSpec{
		configClass: cands[0].class,
		resultClass: cands[0].class, // non-Policy CSPs are a single class
		instanceID:  instanceNode,
		parentID:    bridgeParentID(strings.Join(segs[:len(segs)-2], "/")),
		property:    property,
	}
}

// renderConstants emits typed constants for a policy's ENUM/Flag allowed
// values (integer or string, per the node format).
func renderConstants(b *strings.Builder, p policy) {
	av := p.node.AllowedValues
	if av == nil || (av.Type != "ENUM" && av.Type != "Flag") || len(av.Enum) == 0 {
		return
	}
	goType := cspschema.GoType(p.node)
	if goType != "int64" && goType != "string" {
		return
	}
	type entry struct{ name, spec string }
	var entries []entry
	seen := map[string]bool{}
	for _, e := range av.Enum {
		label := constName(e.Description)
		if label == "" {
			label = constName("Value" + e.Value)
		}
		name := p.goName + label
		if seen[name] {
			continue
		}
		seen[name] = true
		if goType == "string" {
			entries = append(entries, entry{name, fmt.Sprintf("= %q", e.Value)})
		} else if lit, ok := intLiteral(e.Value); ok {
			entries = append(entries, entry{name, fmt.Sprintf("int64 = %s", lit)})
		}
	}
	if len(entries) == 0 {
		return
	}
	fmt.Fprintf(b, "// %s allowed values.\n", p.goName)
	b.WriteString("const (\n")
	for _, e := range entries {
		fmt.Fprintf(b, "\t%s %s\n", e.name, e.spec)
	}
	b.WriteString(")\n\n")
}

func assemble(pkg, doc, body string) ([]byte, error) {
	var b strings.Builder
	b.WriteString(header)
	b.WriteString("\n")
	b.WriteString(doc)
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	if strings.Contains(body, "csp.") {
		b.WriteString("import csp \"github.com/deploymenttheory/go-bindings-wmi/runtime/csp\"\n\n")
	}
	b.WriteString(body)
	return format.Source([]byte(b.String()))
}

func pruneStale(outDir string, written map[string]bool) error {
	if _, err := os.Stat(outDir); err != nil {
		return nil
	}
	return filepath.WalkDir(outDir, func(path string, entry os.DirEntry, err error) error {
		if err != nil || entry.IsDir() || !strings.HasSuffix(path, ".go") || written[path] {
			return err
		}
		content, readErr := os.ReadFile(path)
		if readErr != nil {
			return readErr
		}
		if bytes.HasPrefix(content, []byte(header)) {
			return os.Remove(path)
		}
		return nil
	})
}
