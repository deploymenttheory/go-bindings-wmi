// Command generate turns committed CIM schema snapshots (metadata/cim/*.json)
// into typed Go bindings under bindings/cim/<namespace>: one struct per class
// plus a typed query helper. Self-cleaning and byte-deterministic — running
// it twice produces no diff. Every run validates the snapshots first.
//
//	go run ./cmd/generate                       # regenerate bindings/cim from metadata/cim
//	go run ./cmd/generate validate [dir]        # validate snapshots without generating
//	go run ./cmd/generate diff <old> <new>      # semantic snapshot diff (markdown, for PR bodies)
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/deploymenttheory/go-bindings-wmi/internal/cimschema"
)

const header = "// Code generated by go-bindings-wmi-codegen. DO NOT EDIT.\n"

func main() {
	args := os.Args[1:]
	var err error
	switch {
	case len(args) > 0 && args[0] == "validate":
		metadataDir := "metadata/cim"
		if len(args) > 1 {
			metadataDir = args[1]
		}
		err = runValidate(metadataDir)
	case len(args) > 0 && args[0] == "diff":
		if len(args) != 3 {
			err = fmt.Errorf("usage: generate diff <old.json> <new.json>")
			break
		}
		err = runDiff(args[1], args[2])
	default:
		metadataDir := "metadata/cim"
		if len(args) > 0 {
			metadataDir = args[0]
		}
		err = run(metadataDir, filepath.Join("bindings", "cim"))
	}
	if err != nil {
		fmt.Fprintln(os.Stderr, "generate:", err)
		os.Exit(1)
	}
}

// runValidate checks every snapshot in the metadata dir.
func runValidate(metadataDir string) error {
	snapshots, err := loadSnapshots(metadataDir)
	if err != nil {
		return err
	}
	for path, snapshot := range snapshots {
		if err := snapshot.Validate(); err != nil {
			return fmt.Errorf("%s: %w", path, err)
		}
		fmt.Printf("valid: %s (%s, %d classes)\n", path, snapshot.Namespace, len(snapshot.Classes))
	}
	return nil
}

// runDiff prints the semantic schema diff between two snapshots as markdown.
func runDiff(oldPath, newPath string) error {
	oldSnap, err := loadSnapshot(oldPath)
	if err != nil {
		return err
	}
	newSnap, err := loadSnapshot(newPath)
	if err != nil {
		return err
	}
	if oldSnap.Namespace != newSnap.Namespace {
		return fmt.Errorf("diff: namespaces differ (%s vs %s)", oldSnap.Namespace, newSnap.Namespace)
	}
	fmt.Print(cimschema.Diff(oldSnap, newSnap).Markdown())
	return nil
}

// loadSnapshot reads and parses one snapshot file.
func loadSnapshot(path string) (*cimschema.Snapshot, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var snapshot cimschema.Snapshot
	if err := json.Unmarshal(data, &snapshot); err != nil {
		return nil, fmt.Errorf("%s: %w", path, err)
	}
	return &snapshot, nil
}

// loadSnapshots reads every snapshot in a metadata dir, keyed by path,
// erroring when the dir holds none.
func loadSnapshots(metadataDir string) (map[string]*cimschema.Snapshot, error) {
	entries, err := filepath.Glob(filepath.Join(metadataDir, "*.json"))
	if err != nil {
		return nil, err
	}
	if len(entries) == 0 {
		return nil, fmt.Errorf("no snapshots in %s (run cmd/capture)", metadataDir)
	}
	snapshots := map[string]*cimschema.Snapshot{}
	for _, entry := range entries {
		snapshot, err := loadSnapshot(entry)
		if err != nil {
			return nil, err
		}
		snapshots[entry] = snapshot
	}
	return snapshots, nil
}

func run(metadataDir, outDir string) error {
	snapshots, err := loadSnapshots(metadataDir)
	if err != nil {
		return err
	}
	entries := make([]string, 0, len(snapshots))
	for path := range snapshots {
		entries = append(entries, path)
	}
	sort.Strings(entries)

	// Package names derive from the namespace leaf, so two namespaces with
	// the same leaf would silently overwrite each other's package. Fail fast.
	packages := map[string]string{}
	for _, entry := range entries {
		pkg := packageName(snapshots[entry].Namespace)
		if prev, taken := packages[pkg]; taken {
			return fmt.Errorf("package collision: namespaces %s and %s both map to bindings package %q",
				prev, snapshots[entry].Namespace, pkg)
		}
		packages[pkg] = snapshots[entry].Namespace
	}

	written := map[string]bool{}
	for _, entry := range entries {
		snapshot := snapshots[entry]
		if err := snapshot.Validate(); err != nil {
			return err
		}
		pkg := packageName(snapshot.Namespace)
		pkgDir := filepath.Join(outDir, pkg)
		if err := os.MkdirAll(pkgDir, 0o755); err != nil {
			return err
		}
		source, err := renderPackage(pkg, snapshot)
		if err != nil {
			return fmt.Errorf("%s: %w", entry, err)
		}
		path := filepath.Join(pkgDir, pkg+"_classes.go")
		if err := os.WriteFile(path, source, 0o644); err != nil {
			return err
		}
		written[path] = true
		fmt.Printf("generated %d classes → %s\n", len(snapshot.Classes), path)
	}
	return pruneStale(outDir, written)
}

// packageName derives a Go package name from a CIM namespace
// (root\cimv2 → cimv2).
func packageName(namespace string) string {
	parts := strings.Split(namespace, `\`)
	return strings.ToLower(parts[len(parts)-1])
}

func renderPackage(pkg string, snapshot *cimschema.Snapshot) ([]byte, error) {
	var b strings.Builder
	b.WriteString(header)
	b.WriteString("\n//go:build windows\n\n")
	fmt.Fprintf(&b, "// Package %s binds the %s CIM classes.\n", pkg, snapshot.Namespace)
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	b.WriteString("import wmi \"github.com/deploymenttheory/go-bindings-wmi/runtime/wmi\"\n\n")

	// Pass 1: claim every class-level symbol first, so a method wrapper can
	// never steal a later class's type or query-helper name.
	claimed := map[string]bool{}
	include := map[string]bool{}
	for _, class := range snapshot.Classes {
		goName := exportName(class.Name)
		if goName == "" || claimed[goName] {
			continue // unnameable or a Go-name collision across CIM classes
		}
		claimed[goName] = true
		claimed["Query"+goName] = true
		include[class.Name] = true
	}

	for _, class := range snapshot.Classes {
		if !include[class.Name] {
			continue
		}
		goName := exportName(class.Name)

		// Deduplicate fields by exported Go name (CIM property names can
		// collapse to the same identifier); first occurrence wins.
		type field struct {
			name, goType, cim string
		}
		var fields []field
		seenField := map[string]bool{}
		for _, p := range class.Properties {
			fn := exportName(p.Name)
			if fn == "" || fn == goName || seenField[fn] {
				continue
			}
			seenField[fn] = true
			fields = append(fields, field{fn, cimschema.GoType(p), p.Name})
		}

		fmt.Fprintf(&b, "// %s is the %s CIM class.\n", goName, class.Name)
		fmt.Fprintf(&b, "type %s struct {\n", goName)
		for _, f := range fields {
			fmt.Fprintf(&b, "\t%s %s `cim:%q`\n", f.name, f.goType, f.cim)
		}
		b.WriteString("}\n\n")

		// Typed query helper: run WQL and unmarshal rows into []T.
		fmt.Fprintf(&b, "// Query%s runs the WQL query against the class and decodes each\n", goName)
		fmt.Fprintf(&b, "// instance into a %s. Pass the WHERE clause (or \"\" for all).\n", goName)
		fmt.Fprintf(&b, "func Query%s(svc *wmi.Service, where string) ([]%s, error) {\n", goName, goName)
		fmt.Fprintf(&b, "\tq := \"SELECT * FROM %s\"\n", class.Name)
		b.WriteString("\tif where != \"\" {\n\t\tq += \" WHERE \" + where\n\t}\n")
		b.WriteString("\trows, err := svc.Query(q)\n\tif err != nil {\n\t\treturn nil, err\n\t}\n")
		fmt.Fprintf(&b, "\tout := make([]%s, len(rows))\n", goName)
		if len(fields) > 0 {
			b.WriteString("\tfor i, row := range rows {\n")
			for _, f := range fields {
				// WMI returns values in a different width/shape than the
				// CIM-declared type; coerce rather than assert.
				if coercer, ok := scalarCoercers[f.goType]; ok {
					fmt.Fprintf(&b, "\t\tout[i].%s = wmi.%s(row[%q])\n", f.name, coercer, f.cim)
				} else if coercer, ok := sliceCoercers[f.goType]; ok {
					fmt.Fprintf(&b, "\t\tout[i].%s = wmi.%s(row[%q])\n", f.name, coercer, f.cim)
				} else {
					// `any` and []any: assert, leave zero on mismatch.
					fmt.Fprintf(&b, "\t\tif v, ok := row[%q].(%s); ok {\n\t\t\tout[i].%s = v\n\t\t}\n",
						f.cim, f.goType, f.name)
				}
			}
			b.WriteString("\t}\n")
		}
		b.WriteString("\treturn out, nil\n}\n\n")

		for _, method := range class.Methods {
			renderMethod(&b, claimed, goName, class.Name, method)
		}
	}

	return format.Source([]byte(b.String()))
}

// renderMethod emits a result struct and a typed wrapper for one CIM method.
// Static methods target the class; instance methods take the instance's
// __PATH. Zero-valued in-parameters are omitted from the call so the
// provider applies its defaults (pass values via wmi.Service.ExecMethod's
// map API when explicit zeros are needed).
func renderMethod(b *strings.Builder, claimed map[string]bool, goName, cimClass string, m cimschema.Method) {
	methodName := exportName(m.Name)
	if methodName == "" {
		return
	}
	funcName := goName + methodName
	resultName := funcName + "Result"
	if claimed[funcName] || claimed[resultName] {
		return // symbol collision; the first claimant wins
	}
	claimed[funcName] = true
	claimed[resultName] = true

	type param struct {
		ident, goType, cim string
	}
	var outs []param
	seenOut := map[string]bool{}
	for _, p := range m.Out {
		field := exportName(p.Name)
		if field == "" || seenOut[field] {
			continue
		}
		seenOut[field] = true
		outs = append(outs, param{field, cimschema.ParamGoType(p), p.Name})
	}

	var ins []param
	usedIdents := map[string]bool{"svc": true, "objectPath": true, "in": true, "row": true, "err": true, "out": true, "wmi": true}
	for _, p := range m.In {
		ident := paramIdent(p.Name, usedIdents)
		if ident == "" {
			continue
		}
		usedIdents[ident] = true
		ins = append(ins, param{ident, cimschema.ParamGoType(p), p.Name})
	}

	fmt.Fprintf(b, "// %s holds the out-parameters of %s.%s.\n", resultName, cimClass, m.Name)
	fmt.Fprintf(b, "type %s struct {\n", resultName)
	for _, f := range outs {
		fmt.Fprintf(b, "\t%s %s\n", f.ident, f.goType)
	}
	b.WriteString("}\n\n")

	if m.Static {
		fmt.Fprintf(b, "// %s invokes the static %s.%s method. Zero-valued\n", funcName, cimClass, m.Name)
		b.WriteString("// in-parameters are omitted so the provider applies its defaults.\n")
		fmt.Fprintf(b, "func %s(svc *wmi.Service", funcName)
	} else {
		fmt.Fprintf(b, "// %s invokes %s.%s on the instance at objectPath\n", funcName, cimClass, m.Name)
		b.WriteString("// (the __PATH property of a queried row). Zero-valued in-parameters are\n// omitted so the provider applies its defaults.\n")
		fmt.Fprintf(b, "func %s(svc *wmi.Service, objectPath string", funcName)
	}
	for _, p := range ins {
		fmt.Fprintf(b, ", %s %s", p.ident, p.goType)
	}
	fmt.Fprintf(b, ") (*%s, error) {\n", resultName)

	inArg := "nil"
	if len(ins) > 0 {
		inArg = "in"
		b.WriteString("\tin := map[string]any{}\n")
		for _, p := range ins {
			fmt.Fprintf(b, "\tif %s {\n\t\tin[%q] = %s\n\t}\n", nonZeroExpr(p.ident, p.goType), p.cim, p.ident)
		}
	}
	target := "objectPath"
	if m.Static {
		target = fmt.Sprintf("%q", cimClass)
	}
	fmt.Fprintf(b, "\trow, err := svc.ExecMethod(%s, %q, %s)\n", target, m.Name, inArg)
	b.WriteString("\tif err != nil {\n\t\treturn nil, err\n\t}\n")
	fmt.Fprintf(b, "\tout := &%s{}\n", resultName)
	for _, f := range outs {
		switch coercer, ok := scalarCoercers[f.goType]; {
		case ok:
			fmt.Fprintf(b, "\tout.%s = wmi.%s(row[%q])\n", f.ident, coercer, f.cim)
		case sliceCoercers[f.goType] != "":
			fmt.Fprintf(b, "\tout.%s = wmi.%s(row[%q])\n", f.ident, sliceCoercers[f.goType], f.cim)
		case f.goType == "any":
			fmt.Fprintf(b, "\tout.%s = row[%q]\n", f.ident, f.cim)
		default:
			// []any and future shapes: assert, leave zero on mismatch.
			fmt.Fprintf(b, "\tif v, ok := row[%q].(%s); ok {\n\t\tout.%s = v\n\t}\n", f.cim, f.goType, f.ident)
		}
	}
	b.WriteString("\treturn out, nil\n}\n\n")
}

// nonZeroExpr renders the zero-value guard for an in-parameter.
func nonZeroExpr(ident, goType string) string {
	switch {
	case goType == "bool":
		return ident
	case goType == "string":
		return ident + ` != ""`
	case goType == "any" || strings.HasPrefix(goType, "[]"):
		return ident + " != nil"
	default:
		return ident + " != 0"
	}
}

// paramIdent derives a Go parameter identifier from a CIM parameter name,
// avoiding keywords, predeclared identifiers, and already-used names.
func paramIdent(name string, used map[string]bool) string {
	exported := exportName(name)
	if exported == "" {
		return ""
	}
	runes := []rune(exported)
	if runes[0] >= 'A' && runes[0] <= 'Z' {
		runes[0] = runes[0] - 'A' + 'a'
	}
	ident := string(runes)
	if reservedIdents[ident] {
		ident += "Arg"
	}
	for used[ident] {
		ident += "_"
	}
	return ident
}

// reservedIdents holds Go keywords and predeclared identifiers a parameter
// name must not shadow (a param named `string` would break the signature).
var reservedIdents = func() map[string]bool {
	words := []string{
		"break", "case", "chan", "const", "continue", "default", "defer", "else",
		"fallthrough", "for", "func", "go", "goto", "if", "import", "interface",
		"map", "package", "range", "return", "select", "struct", "switch", "type", "var",
		"any", "bool", "byte", "comparable", "complex64", "complex128", "error",
		"float32", "float64", "int", "int8", "int16", "int32", "int64", "rune",
		"string", "uint", "uint8", "uint16", "uint32", "uint64", "uintptr",
		"nil", "true", "false", "iota",
		"append", "cap", "clear", "close", "complex", "copy", "delete", "imag",
		"len", "make", "max", "min", "new", "panic", "print", "println", "real", "recover",
	}
	m := make(map[string]bool, len(words))
	for _, w := range words {
		m[w] = true
	}
	return m
}()

// scalarCoercers maps a scalar Go field type to its runtime coercion helper.
// Types absent here and in sliceCoercers (any, []any) fall back to a type
// assertion.
var scalarCoercers = map[string]string{
	"string":  "AsString",
	"bool":    "AsBool",
	"int8":    "AsInt8",
	"int16":   "AsInt16",
	"int32":   "AsInt32",
	"int64":   "AsInt64",
	"uint8":   "AsUint8",
	"uint16":  "AsUint16",
	"uint32":  "AsUint32",
	"uint64":  "AsUint64",
	"float32": "AsFloat32",
	"float64": "AsFloat64",
}

// sliceCoercers maps a slice Go field type to its runtime coercion helper
// (the runtime decodes SAFEARRAY properties to []any; these coerce each
// element).
var sliceCoercers = map[string]string{
	"[]string":  "AsStringSlice",
	"[]bool":    "AsBoolSlice",
	"[]int8":    "AsInt8Slice",
	"[]int16":   "AsInt16Slice",
	"[]int32":   "AsInt32Slice",
	"[]int64":   "AsInt64Slice",
	"[]uint8":   "AsUint8Slice",
	"[]uint16":  "AsUint16Slice",
	"[]uint32":  "AsUint32Slice",
	"[]uint64":  "AsUint64Slice",
	"[]float32": "AsFloat32Slice",
	"[]float64": "AsFloat64Slice",
}

// exportName upper-cases the first letter and strips characters Go rejects in
// identifiers (CIM names are already CamelCase ASCII).
func exportName(name string) string {
	var out []rune
	upperNext := true
	for _, r := range name {
		if r == '_' {
			upperNext = true
			continue
		}
		if upperNext && r >= 'a' && r <= 'z' {
			r = r - 'a' + 'A'
		}
		upperNext = false
		out = append(out, r)
	}
	return string(out)
}

// pruneStale removes generated files this run did not write.
func pruneStale(outDir string, written map[string]bool) error {
	if _, err := os.Stat(outDir); err != nil {
		return nil
	}
	return filepath.WalkDir(outDir, func(path string, entry os.DirEntry, err error) error {
		if err != nil || entry.IsDir() || !strings.HasSuffix(path, ".go") || written[path] {
			return err
		}
		content, readErr := os.ReadFile(path)
		if readErr != nil {
			return readErr
		}
		if bytes.HasPrefix(content, []byte(header)) {
			return os.Remove(path)
		}
		return nil
	})
}
